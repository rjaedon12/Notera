export type ThemeId = keyof typeof THEMES;
export const DEFAULT_THEME: ThemeId = 'monochrome';

const THEME_STORAGE_KEY = 'notera-color-theme';

/** All CSS variable keys to set on each theme switch */
const CSS_VAR_KEYS: (keyof ThemeVariant)[] = [
  '--bg-base', '--bg-surface', '--bg-elevated',
  '--accent-color', '--accent-light', '--accent-muted',
  '--text-primary', '--text-muted', '--border',
  '--background', '--foreground',
  '--card', '--card-foreground',
  '--popover', '--popover-foreground',
  '--muted', '--muted-foreground',
  '--input', '--input-foreground',
  '--primary', '--primary-foreground',
  '--secondary', '--secondary-foreground',
  '--accent', '--accent-foreground',
  '--destructive', '--destructive-foreground',
  '--ring',
  '--sidebar-bg', '--sidebar-border', '--notes-sidebar-bg',
  '--glass-fill', '--glass-fill-hover', '--glass-border',
  '--glass-highlight', '--glass-shadow', '--glass-shadow-hover',
  '--bg-glow-1', '--bg-glow-2', '--bg-glow-3',
];

/** Apply a color theme's CSS variables to <html> and persist in localStorage.
 *  Picks dark or light variant based on whether <html> has the `.dark` class. */
export function applyTheme(id: ThemeId, mode?: 'light' | 'dark'): void {
  const theme = THEMES[id];
  if (!theme) return;

  const root = document.documentElement;
  const isDark = mode ? mode === 'dark' : root.classList.contains('dark');
  const variant = isDark ? theme.dark : theme.light;

  for (const key of CSS_VAR_KEYS) {
    root.style.setProperty(key, variant[key]);
  }

  try {
    localStorage.setItem(THEME_STORAGE_KEY, id);
  } catch {
    // localStorage may be unavailable (private browsing, quota, etc.)
  }
}

/** Read the saved theme from localStorage, falling back to DEFAULT_THEME. */
export function getSavedTheme(): ThemeId {
  try {
    const saved = localStorage.getItem(THEME_STORAGE_KEY);
    if (saved && saved in THEMES) {
      return saved as ThemeId;
    }
  } catch {
    // localStorage unavailable
  }
  return DEFAULT_THEME;
}

/** Get the list of theme IDs in display order */
export function getThemeIds(): ThemeId[] {
  return Object.keys(THEMES) as ThemeId[];
}
